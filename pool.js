// 导出一个获取所有地图的函数（平铺所有轮次的地图）
function getAllMaps() {
    let allMaps = [];
    for (const round in poolData) {
        if (poolData.hasOwnProperty(round)) {
            // 为每个地图添加轮次信息
            const roundMaps = poolData[round].map(map => ({
                ...map,
                round: round
            }));
            allMaps = allMaps.concat(roundMaps);
        }
    }
    return allMaps;
}

